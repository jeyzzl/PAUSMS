package aca.puestos;

public class Seccion {
	
	int categoria_id;
	String descripcion_nueva;
	int seccion_id;
	
	
	public int getCategoria_id() {
		return categoria_id;
	}



	public void setCategoria_id(int categoria_id) {
		this.categoria_id = categoria_id;
	}



	public String getDescripcion_nueva() {
		return descripcion_nueva;
	}



	public void setDescripcion_nueva(String descripcion_nueva) {
		this.descripcion_nueva = descripcion_nueva;
	}



	public int getSeccion_id() {
		return seccion_id;
	}



	public void setSeccion_id(int seccion_id) {
		this.seccion_id = seccion_id;
	}







}
