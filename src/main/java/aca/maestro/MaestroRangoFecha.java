package aca.maestro;

public class MaestroRangoFecha {
	private String year;
	private String fecha;
	
	public MaestroRangoFecha(){

		year		= "0";
		fecha		= "-";					
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getFecha() {
		return fecha;
	}

	public void setFecha(String fecha) {
		this.fecha = fecha;
		}
	}

	