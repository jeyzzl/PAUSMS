package aca.proyectos;

/**
 *
 * @author Daniel
 */
public class DptoMetas {

    private int id;
    private int id_objetivo;
    private String descripcion;
    private String estado;
    private String fecha_creacion;
    private String descripcion_objetivo;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the id_objetivo
     */
    public int getId_objetivo() {
        return id_objetivo;
    }

    /**
     * @param id_objetivo the id_objetivo to set
     */
    public void setId_objetivo(int id_objetivo) {
        this.id_objetivo = id_objetivo;
    }

    /**
     * @return the descripcion
     */
    public String getDescripcion() {
        return descripcion;
    }

    /**
     * @param descripcion the descripcion to set
     */
    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    /**
     * @return the estado
     */
    public String getEstado() {
        return estado;
    }

    /**
     * @param estado the estado to set
     */
    public void setEstado(String estado) {
        this.estado = estado;
    }

    /**
     * @return the fecha_creacion
     */
    public String getFecha_creacion() {
        return fecha_creacion;
    }

    /**
     * @param fecha_creacion the fecha_creacion to set
     */
    public void setFecha_creacion(String fecha_creacion) {
        this.fecha_creacion = fecha_creacion;
    }

    /**
     * @return the descripcion_objetivo
     */
    public String getDescripcion_objetivo() {
        return descripcion_objetivo;
    }

    /**
     * @param descripcion_objetivo the descripcion_objetivo to set
     */
    public void setDescripcion_objetivo(String descripcion_objetivo) {
        this.descripcion_objetivo = descripcion_objetivo;
    }

}
