package aca.reportes;

public class AltasBajas {
	public String planId;
	public String carreraId;
	public String facultadId;
	public String matricula;
	public String nombre;

	
	public AltasBajas(String planId, String carreraId, String facultadId, String matricula, String nombre) {
		this.planId = planId;
		this.carreraId = carreraId;
		this.facultadId = facultadId;
		this.matricula = matricula;
		this.nombre = nombre;
	}
}